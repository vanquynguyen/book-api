     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $book = Book::find($request->id);
        $cart = Cart::add([
            'id' => $request->id, 
            'name' => $book->title, 
            'qty' => 1, 
            'price' => 9.99, 
            'options' => [
                'image' => $book->image
            ]
        ]);

        return response()->json($cart);
    }

